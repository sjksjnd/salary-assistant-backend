async function seed() {
  for (const item of configItems) {
    try {
      await queryInsert(
        'INSERT INTO config_items (`key`, value, description) VALUES (?, ?, ?) ON DUPLICATE KEY UPDATE value = VALUES(value), description = VALUES(description)',
        [item.key, JSON.stringify(item.value), item.description]
      );
      console.log(`✓ Config item: ${item.key}`);
    } catch (err) {
      console.error(`✗ Error seeding ${item.key}:`, err.message);
      throw err;
    }
  }
}

module.exports = { seed };
